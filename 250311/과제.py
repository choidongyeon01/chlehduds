from collections import Counter
from konlpy.tag import Okt

# 텍스트 전처리
def preprocess_text(text):
    # 특수문자 제거
    text = text.replace("…", "").replace("?", "")
    # 불용어 처리 등 추가 전처리 가능
    return text

# 텍스트에서 명사 추출
okt = Okt()

def extract_keywords(text):
    # 텍스트 전처리
    text = preprocess_text(text)
    # 명사 추출
    nouns = okt.nouns(text)
    return nouns

# 모든 텍스트에서 명사 추출
all_nouns = []
for sentence in data:
    all_nouns.extend(extract_keywords(sentence))

# 단어 빈도 계산
word_counts = Counter(all_nouns)

# 상위 10개 키워드 출력
print(word_counts.most_common(30))

# 텍스트 데이터로부터 성형 관련 키워드 추출
keywords = [
    "눈", "코", "필러", "쌍수", "쌍커풀", "쌍커플", "메부리코성형", "지방이식", "눈매성형", "앞트임",
    "뒷트임", "눈성형", "짝짝이", "속눈썹", "질성형", "소음순"
]

# 데이터 전처리 (특수문자, 불용어 제거 등)
def preprocess_text(text):
    text = re.sub(r'[^\w\s]', '', text)  # 특수문자 제거
    text = text.lower()  # 소문자 변환
    return text

# 키워드 카운팅
vectorizer = CountVectorizer(vocabulary=keywords)
X = vectorizer.fit_transform([preprocess_text(doc) for doc in data])
word_counts = X.toarray().sum(axis=0)

# 데이터프레임으로 정리
keyword_counts = dict(zip(keywords, word_counts))

# 워드클라우드 생성
wordcloud = WordCloud(font_path="malgun.ttf", width=800, height=400, background_color='white').generate_from_frequencies(keyword_counts)

